module.exports.logoutUser = async (req, res, next) => {
    try {
        // Clear cookie
        res.clearCookie('token', { httpOnly: true, secure: true });

        // Extract token from cookie or headers
        let token = req.cookies?.token;  // Safe access
        if (!token && req.headers.authorization) {
            const authHeader = req.headers.authorization;
            if (authHeader.startsWith('Bearer ')) {
                token = authHeader.split(' ')[1]; // Extract token
            }
        }

        // If no token is found, return error
        if (!token) {
            return res.status(400).json({ message: 'No token found, already logged out' });
        }

        // Blacklist the token
        await blackListTokenModel.create({ token });

        return res.status(200).json({ message: 'Logged out successfully' });
    } catch (error) {
        console.error('Logout Error:', error);
        return res.status(500).json({ message: 'Internal server error' });
    }
};
